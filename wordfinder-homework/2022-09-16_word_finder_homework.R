for(word in words){
  lines_found<-grep(word,lines)
  if(length(lines_found)>0){
    for(line_found in lines_found){
      word_found<-35-nchar(sub(paste0("^.+",word),"",lines[line_found]))-nchar(word)
      cat(paste0(word," - HORISONTAL line ",line_found," character ",word_found,"\n"))
    }
  }
  
  lines_found<-grep(word,lines_transposed)
  if(length(lines_found)>0){
    for(line_found in lines_found){
      
      word_found<-35-nchar(sub(paste0("^.+",word),"",lines_transposed[line_found]))-nchar(word)
      cat(paste0(word," - VERTICAL line ",word_found," character ",line_found,"\n"))
    }
  }
  
  
  
  lines_found<-grep(word,lines_diagonalized)
  if(length(lines_found)>0){
    for(line_found in lines_found){
      diagonal_down_character_count<-nchar(lines_diagonalized[line_found]) - nchar(word) - nchar(sub(paste0("^.+",word),"",lines_diagonalized[line_found]))
      if(line_found > 35){
        real_line_count <- line_found - 35 + diagonal_down_character_count
        character_count <- diagonal_down_character_count
      }else{
        real_line_count <- diagonal_down_character_count+1
        character_count <- 35 - (line_found - diagonal_down_character_count)
      }
      
      cat(paste0(word," - DIAGONAL line ",real_line_count," character ",character_count,"\n"))
    }
  }
  
  
  
  lines_found<-grep(word,lines_diagonalized_up)
  if(length(lines_found)>0){
    for(line_found in lines_found){
      diagonal_down_character_count<-nchar(lines_diagonalized_up[line_found]) - nchar(word) - nchar(sub(paste0("^.+",word),"",lines_diagonalized_up[line_found]))
      if(line_found > 35){
        real_line_count <- line_found - 35 + diagonal_down_character_count
        character_count <- diagonal_down_character_count
      }else{
        real_line_count <- diagonal_down_character_count+1
        character_count <- 35 - (line_found - diagonal_down_character_count)
      }
      
      cat(paste0(word," - DIAGONAL-UP line ",
                 real_line_count," 
                 character ",character_count,
                 "\n"))
    }
  }
  
  
  
