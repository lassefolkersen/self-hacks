lines_diagonalized<-vector()
for(i in 1:(35+34)){
  if(i <= 35){
    lines_diagonalized<-c(lines_diagonalized, paste0(rep("X",i),collapse=""))
  }else{
    ix <- 35 - (i-35) 
    lines_diagonalized<-c(lines_diagonalized, paste0(rep("X",ix),collapse=""))
  }
  
  
  for(k in 1:nchar(lines_diagonalized[i])){
    if(i  < 35){
      start_line <-k
    }else{
      start_line <- k  + (i - 35)
    }
    letter_to_insert<-substr(lines[start_line],35-i+start_line,35-i+start_line)
    substr(lines_diagonalized[i],k,k)<-letter_to_insert
  }
  
}




lines_diagonalized_up<-vector()
for(i in 1:(35+34)){
  if(i <= 35){
    lines_diagonalized_up<-c(lines_diagonalized_up, paste0(rep("X",i),collapse=""))
  }else{
    ix <- 35 - (i-35) 
    lines_diagonalized_up<-c(lines_diagonalized_up, paste0(rep("X",ix),collapse=""))
  }
  
  
  
  
  for(k in 1:nchar(lines_diagonalized_up[i])){
    if(i  < 35){
      start_line <-  nchar(lines_diagonalized_up[i]) -k +1
    }else{
      start_line <- 35 - k + 1
    }
    letter_to_insert<-substr(lines[start_line],1+i-start_line,1+i-start_line)
    substr(lines_diagonalized_up[i],k,k)<-letter_to_insert
  }
  
}




